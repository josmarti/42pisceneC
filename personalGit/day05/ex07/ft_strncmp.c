int		ft_strncmp(char *s1, char *s2, int n)
{
	int i;

	i = 0;
	while (i < n - 1)
	{
		while ((*s1) && (*s2))
		{
			if (*s1 != *s2)
			{
				i = (*s1 - *s2);
				return (i);
			}
			i++;
			s1++;
			s2++;
		}
	}
	i = (*s1 - *s2);
	return (i);
}
