#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	while (*str)
	{
		ft_putchar(*str);
		str++;
	}
}

void	ft_generic(void)
{
	char *c;

	c = "Tut tut ; Tut tut\n";
	ft_putstr(c);
}
